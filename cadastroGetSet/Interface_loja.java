    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        long id = Long.parseLong(txtFindById.getText());
        
        Produto p = loja.buscarPorId(id);

        txtExibirId.setText("ID: "+String.valueOf(p.getId()));
        txtExibirNome.setText("Produto: "+p.getNome());
        txtExibirPreco.setText("Pre√ßo: RS "+String.valueOf(p.getPreco()));
        txtExibirQuantidade.setText("Qtd: "+String.valueOf(p.getQuantidade()));
    }//GEN-LAST:event_btnMostrarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        long id = Long.parseLong(txtFindById.getText());
        String nome = txtNome.getText();
        double preco = Double.parseDouble(txtPreco.getText());
        int quantidade = Integer.parseInt(txtQuantidade.getText());
        
        loja.atualizarProduto(id, nome, preco, quantidade);
        
        txtExibirId.setText("");
        txtExibirNome.setText("");
        txtExibirPreco.setText("");
        txtExibirQuantidade.setText("");
        
        
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        long id = Long.parseLong(txtFindById.getText());
        loja.excluirProduto(id);
        txtExibirNome.setText("Produto excluido com sucesso");
    }//GEN-LAST:event_btnExcluirActionPerformed

